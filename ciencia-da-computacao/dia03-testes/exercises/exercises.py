def list_numbers(maximum: int):
    result = list()
    for number in range(1, maximum + 1):
        if number % 3 == 0 and number % 5 == 0:
            result.append("FizBuzz")
        elif number % 3 == 0:
            result.append("Fizz")
        elif number % 5 == 0:
            result.append("Buzz")
        else:
            result.append(number)
    return result


